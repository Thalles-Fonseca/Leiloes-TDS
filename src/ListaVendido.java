   private void carregarVendas() {
        DefaultTableModel modelo = (DefaultTableModel) tblVendidos.getModel();
        modelo.setRowCount(0); // limpa a tabela antes de carregar

        String sql = "SELECT id, nome, valor, status FROM produtos WHERE status = 'Vendido'";

        try (Connection conn = Conexao.getConnection();
             PreparedStatement stmt = conn.prepareStatement(sql);
             ResultSet rs = stmt.executeQuery()) {

            while (rs.next()) {
                int id = rs.getInt("id");
                String nome = rs.getString("nome");
                double valor = rs.getDouble("valor");
                String status = rs.getString("status");

                modelo.addRow(new Object[]{id, nome, valor, status});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar vendas: " + e.getMessage());
        }}
